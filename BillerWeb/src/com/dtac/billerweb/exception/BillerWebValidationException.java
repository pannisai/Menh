package com.dtac.billerweb.exception;

public class BillerWebValidationException extends RuntimeException {
	public BillerWebValidationException() {
		super();
	}

	public BillerWebValidationException(String message) {
		super(message);
	}

	public BillerWebValidationException(String message, Throwable cause) {
		super(message, cause);
	}

	public BillerWebValidationException(Throwable cause) {
		super(cause);
	}
}
