package mfs.exception;

public class NotFoundBillerException extends Exception {
	  public NotFoundBillerException() { super(); }
	  public NotFoundBillerException(String message) { super(message); }
	  public NotFoundBillerException(String message, Throwable cause) { super(message, cause); }
	  public NotFoundBillerException(Throwable cause) { super(cause); }
}
