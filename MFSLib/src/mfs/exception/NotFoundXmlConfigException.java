package mfs.exception;

public class NotFoundXmlConfigException extends Exception {
	  public NotFoundXmlConfigException() { super(); }
	  public NotFoundXmlConfigException(String message) { super(message); }
	  public NotFoundXmlConfigException(String message, Throwable cause) { super(message, cause); }
	  public NotFoundXmlConfigException(Throwable cause) { super(cause); }
}
